    private void check(){
        File f = new File(file);
        if(f.exists()){
            List<MatHang> list = IOFile.doc(file);
            if(list.size()> 0){
                MatHang.setsMa(list.get(list.size()-1).getMa()+1);
            }
        }
    }
private List<MatHang> getAll(){
    List<MatHang> list = new ArrayList<>();
    for(int i = 0; i < model.getRowCount(); i++){
        int ma = Integer.parseInt(model.getValueAt(i, 0).toString());
        String ten = model.getValueAt(i,1 ).toString();
        String nhom = model.getValueAt(i,2).toString();
        int giaMua = Integer.parseInt(model.getValueAt(i, 3).toString());
        int giaBan = Integer.parseInt(model.getValueAt(i, 4).toString());
        MatHang mh= new MatHang(ma, ten, nhom, giaMua, giaBan);
        list.add(mh);
    }
    return list;
}
