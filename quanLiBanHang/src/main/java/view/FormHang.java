    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(add){
            MatHang mh = new MatHang();
            boolean isOk = true;
            int ma = Integer.parseInt(jTextField1.getText());
            String ten = jTextField2.getText();
            try {
                if(ten.length() == 0){
                    throw new ArithmeticException("Khong duoc de trong!");
                }
            } catch (ArithmeticException e) {
                isOk = false;
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Ten khong hop le!");
            }
            
            String nhom = jComboBox1.getSelectedItem().toString();
            
            int giaMua = 0;
            try {
                giaMua = Integer.parseInt(jTextField3.getText());
            } catch (NumberFormatException e) {
                isOk = false;
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Gia mua khong hop le!");
            }
            
            int giaBan = 0;
            try {
                giaBan = Integer.parseInt(jTextField4.getText());
            } catch (NumberFormatException e) {
                isOk = false;
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Gia ban khong hop le!");
            }
            if(isOk){
                mh = new MatHang(ma, ten, nhom, giaMua, giaBan);
                model.addRow(mh.toObject());
                add = false;
                jButton1.setEnabled(add);
                jButton2.setEnabled(!add);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


