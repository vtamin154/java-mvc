    private void loadID() {
        List<MatHang> mh = IOFile.doc("src/main/java/controller/MH.txt");
        jComboBox2.removeAllItems();
        for (MatHang i : mh) {
            jComboBox2.addItem(i.getMa() + "");
        }
        List<Nhanvien> nv = IOFile.doc("src/main/java/controller/NV.txt");
        jComboBox1.removeAllItems();
        for (Nhanvien i : nv) {
            jComboBox1.addItem(i.getMa() + "");
        }
    }

    private List<QuanLiBanHang> getAll() {
        List<QuanLiBanHang> list = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            int mnv = Integer.parseInt(model.getValueAt(i, 0).toString());
            int mmh = Integer.parseInt(model.getValueAt(i, 2).toString());
            Nhanvien nv = getNhanVien(mnv);
            MatHang mh = getMatHang(mmh);
            int soLuong = Integer.parseInt(model.getValueAt(i, 3).toString());
            QuanLiBanHang ql = new QuanLiBanHang(nv, mh, soLuong);
            list.add(ql);

        }
        return list;
    }

    private Nhanvien getNhanVien(int mnv) {
        List<Nhanvien> list = IOFile.doc("src/main/java/controller/NV.txt");
        for (Nhanvien i : list) {
            if (i.getMa() == mnv) {
                return i;
            }

        }
        return null;
    }

    private MatHang getMatHang(int mmh) {
        List<MatHang> list = IOFile.doc("src/main/java/controller/MH.txt");
        for (MatHang i : list) {
            if (i.getMa() == mmh) {
                return i;
            }

        }
        return null;
    }

