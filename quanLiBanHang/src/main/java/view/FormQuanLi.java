    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int mnv = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int mmh = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        boolean isOk = true;
        if (!tontai(mnv, mmh)) {
            Nhanvien nv = getNhanVien(mnv);
            MatHang mh = getMatHang(mmh);
            int soLuong = 0;
            try {
                soLuong = Integer.parseInt(jTextField1.getText());
            } catch (NumberFormatException e) {
                isOk = false;
                JOptionPane.showMessageDialog(this, "So luong khong hop le!");
                e.printStackTrace();
            }
            if (isOk) {
                QuanLiBanHang ql = new QuanLiBanHang(nv, mh, soLuong);
                model.addRow(ql.toObject());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        List<QuanLiBanHang> list = getAll();
        if(jRadioButton1.isSelected()){
            
            list.sort(new Comparator<QuanLiBanHang>(){
                public int compare(QuanLiBanHang q1, QuanLiBanHang q2){
                    String ten1 = q1.getMh().getTen();
                    String ten2 = q2.getMh().getTen();
                    if(ten1.compareToIgnoreCase(ten2) == 0){
                        return q1.getSoLuong()-q2.getSoLuong();
                    }
                    return ten1.compareToIgnoreCase(ten2);
                }
            });
        }
        else{
            list.sort(new Comparator<QuanLiBanHang>(){
                 public int compare(QuanLiBanHang q1, QuanLiBanHang q2){
                    String ten1 = q1.getNv().getTen();
                    String ten2 = q2.getNv().getTen();
                    String name1 = ten1.substring(ten1.lastIndexOf(" ")+1)+ten1;
                    String name2 = ten2.substring(ten2.lastIndexOf(" ")+1)+ten2;
                    if(name1.compareToIgnoreCase(name2) == 0){
                        return q1.getSoLuong()-q2.getSoLuong();
                    }
                    return name1.compareToIgnoreCase(name2);
                }
            });
        }
        model.setRowCount(0);
        for(QuanLiBanHang i : list){
            model.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton4ActionPerformed
    private boolean tontai(int mnv, int mmh) {
        List<QuanLiBanHang> list = getAll();
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i).getNv().getMa() == mnv && list.get(i).getMh().getMa() == mmh) {
                int soLuong = Integer.parseInt(jTextField1.getText()) + list.get(i).getSoLuong();
                model.setValueAt(soLuong, i, 3);
                return true;
            }
        }
        return false;
    }

