    private void loadID() {
        List<KhachHang> kh = IOFile.doc("src/main/java/io/KH.txt");
        jComboBox1.removeAllItems();
        for (KhachHang i : kh) {
            jComboBox1.addItem(i.getMa() + "");
        }
        List<DichVu> dv = IOFile.doc("src/main/java/io/DV.txt");
        jComboBox2.removeAllItems();
        for (DichVu i : dv) {
            jComboBox2.addItem(i.getMa() + "");
        }
    }

    private KhachHang getKH(int ma) {
        List<KhachHang> list = IOFile.doc("src/main/java/io/KH.txt");
        for (KhachHang i : list) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private DichVu getDV(int ma) {
        List<DichVu> list = IOFile.doc("src/main/java/io/DV.txt");
        for (DichVu i : list) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private List<HoaDon> getAll() {
        List<HoaDon> list = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            int mkh = Integer.parseInt(model.getValueAt(i, 0).toString());
            int mdv = Integer.parseInt(model.getValueAt(i, 2).toString());
            KhachHang kh = getKH(mkh);
            DichVu dv = getDV(mdv);
            int sl = Integer.parseInt(model.getValueAt(i, 3).toString());
            HoaDon hd = new HoaDon(kh, dv, sl);
            list.add(hd);
        }
        return list;
    }

