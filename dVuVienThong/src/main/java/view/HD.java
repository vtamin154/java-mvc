    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int mkh = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int mdv = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        if (tontai(mkh, mdv) == false) {
            if (count(mkh) >= 5) {
                JOptionPane.showMessageDialog(this, "No");
            } else {
                KhachHang kh = getKH(mkh);
                DichVu dv = getDV(mdv);
                int sl = Integer.parseInt(jTextField1.getText());
                HoaDon hd = new HoaDon(kh, dv, sl);
                model.addRow(hd.toObject());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private boolean tontai(int mkh, int mdv) {
        List<HoaDon> list = getAll();
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i).getKh().getMa() == mkh && list.get(i).getDv().getMa() == mdv) {
                int sl = list.get(i).getSoLuong() + Integer.parseInt(jTextField1.getText());
                model.setValueAt(sl, i, 3);
                return true;
            }
        }
        return false;
    }

    private int count(int mkh) {
        int c = 0;
        List<HoaDon> kh = getAll();
        for (HoaDon i : kh) {
            if (i.getKh().getMa() == mkh) {
                c++;
            }
        }
        return c;
    }

