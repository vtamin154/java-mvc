    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        add = true;
        List<KhachHang> list = getAll();
        if (list.size() > 0) {
            jTextField1.setText(list.get(list.size() - 1).getMa() + 1 + "");
        } else
            jTextField1.setText(KhachHang.getsMa() + "");
    }//GEN-LAST:event_jButton2ActionPerformed
    private List<KhachHang> getAll() {
        List<KhachHang> list = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            int ma = Integer.parseInt(model.getValueAt(i, 0).toString());
            String ten = model.getValueAt(i, 1).toString();
            String dc = model.getValueAt(i, 2).toString();
            String loai = model.getValueAt(i, 3).toString();
            KhachHang kh = new KhachHang(ma, ten, dc, loai);
            list.add(kh);
        }
        return list;
    }
