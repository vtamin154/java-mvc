    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        List<KhachHang> list = IOFile.doc(file);
        model.setRowCount(0);
        for (KhachHang i : list) {
            model.addRow(i.toObject());
        }
        check();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (add) {
            KhachHang kh = new KhachHang();
            boolean isOK = true;
            int ma = Integer.parseInt(jTextField1.getText());
            String ten = jTextField2.getText();
            if (ten.length() == 0 || ten.matches("[^a-zA-Z]+")) {
                isOK = false;
                JOptionPane.showMessageDialog(this, "Ten khong hop le!");
            }
            String dc = jTextField3.getText();
            if (dc.length() == 0) {
                isOK = false;
                JOptionPane.showMessageDialog(this, "Dia chi khong hop le!");
            }
            String l = jComboBox1.getSelectedItem().toString();
            if (isOK) {
                kh = new KhachHang(ma, ten, dc, l);
                model.addRow(kh.toObject());
                add = false;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


