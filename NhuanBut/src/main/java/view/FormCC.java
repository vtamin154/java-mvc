    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jRadioButton1.isSelected()){
            list.sort(new Comparator<TinhCong>(){
                public int compare(TinhCong t1, TinhCong t2){
                    String ten1 = t1.getPv().getTen();
                    String ten2 = t2.getPv().getTen();
                    String name1 =ten1.substring(ten1.lastIndexOf(" ")+1)+ten1;
                    String name2 =ten2.substring(ten2.lastIndexOf(" ")+1)+ten2;
                    if(name1.compareToIgnoreCase(name2) == 0){
                        return t1.getSoLuong()-t2.getSoLuong();
                    }
                    return name1.compareToIgnoreCase(name2);
                }
            });
        }
        else{
            list.sort(new Comparator<TinhCong>(){
                public int compare(TinhCong t1, TinhCong t2){
                    String ten1 = t1.getBv().getTen();
                    String ten2 = t2.getBv().getTen();
                    
                    if(ten1.compareToIgnoreCase(ten2) == 0){
                        return t1.getSoLuong()-t2.getSoLuong();
                    }
                    return ten1.compareToIgnoreCase(ten2);
                }
            });
        }
        model.setRowCount(0);
        list.forEach(x ->{
            model.addRow(x.toObject());
        });
    }//GEN-LAST:event_jButton4ActionPerformed

    private boolean tontai(int mpv, int mbv){
        for(int i = 0; i < list.size(); i++){
            if(list.get(i).getPv().getMa() == mpv && list.get(i).getBv().getMa() == mbv){
                int sl = Integer.parseInt(jTextField1.getText())
                        +Integer.parseInt(model.getValueAt(i, 3).toString());
                model.setValueAt(sl, i, 3);
                return true;
            }
                
        }
        return false;
    }
    private int count(int mpv){
        int c = 0;
        for(TinhCong i : list){
            if(i.getPv().getMa() == mpv){
                c++;
            }
        }
        return c;
    }

