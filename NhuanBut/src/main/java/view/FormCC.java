    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int mpv = jComboBox1.getSelectedIndex();
        int mbv = jComboBox2.getSelectedIndex();
        boolean isOk = true;
        PhongVien pv = listPV.get(mpv);
        BaiViet bv = listBV.get(mbv);
        int soLuong  = Integer.parseInt(jTextField1.getText());
        TinhCong tc = new TinhCong(pv, bv, soLuong);
        if(!tontai(pv.getMa(), bv.getMa())){
            if(count(pv.getMa()) >= 5){
                JOptionPane.showMessageDialog(this, "Khong duoc them!");
            }
            else{
                model.addRow(tc.toObject());
            list.add(tc);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

