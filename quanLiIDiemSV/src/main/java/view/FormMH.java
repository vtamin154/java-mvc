    private void check() {
        File f = new File(file);
        if (f.exists()) {
            List<MonHoc> list = IOFile.doc(file);
            if (list.size() > 0) {
                MonHoc.setsMa(list.get(list.size() - 1).getMa() + 1);
            }
        }
    }

    private List<MonHoc> getAll() {
        List<MonHoc> list = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            int ma = Integer.parseInt(model.getValueAt(i, 0).toString());
            String ten = model.getValueAt(i, 1).toString();
            int dvht = Integer.parseInt(model.getValueAt(i, 2).toString());
            String loai = model.getValueAt(i, 3).toString();
            MonHoc mh = new MonHoc(ma, ten, dvht, loai);
            list.add(mh);
        }
        return list;
    }

