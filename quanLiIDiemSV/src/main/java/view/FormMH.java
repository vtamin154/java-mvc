    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (add) {
            MonHoc mh = new MonHoc();
            boolean isOk = true;
            int ma = Integer.parseInt(jTextField1.getText());
            String ten = jTextField2.getText();
            try {
                checkName(ten);
            } catch (Exception e) {
                isOk = false;
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Ten khong hop le!");
            }
            int dvht = 0;
            try {
                dvht = Integer.parseInt(jTextField3.getText());

            } catch (NumberFormatException e) {
                isOk = false;
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "So dvht khong hop le!");
            }
            String loai = jComboBox1.getSelectedItem().toString();

            if (isOk) {
                mh = new MonHoc(ma, ten, dvht, loai);
                model.addRow(mh.toObject());
                add = false;
                jButton1.setEnabled(add);
                jButton2.setEnabled(!add);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private void checkName(String ten) throws Exception {
        if (ten.length() == 0) {
            throw new ArithmeticException("Khong duoc de trong!");
        }
    }

