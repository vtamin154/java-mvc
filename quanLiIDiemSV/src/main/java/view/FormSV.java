    private void check() {
        File f = new File(file);
        if (f.exists()) {
            List<SinhVien> list = IOFile.doc(file);
            if (list.size() > 0) {
                SinhVien.setsMa(list.get(list.size() - 1).getMa() + 1);
            }
        }
    }

    private List<SinhVien> getAll() {
        List<SinhVien> list = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            int ma = Integer.parseInt(model.getValueAt(i, 0).toString());
            String ten = model.getValueAt(i, 1).toString();
            String dc = model.getValueAt(i, 2).toString();
            String ns = model.getValueAt(i, 3).toString();
            String lop = model.getValueAt(i, 4).toString();
            SinhVien sv = new SinhVien(ma, ten, dc, ns, lop);
            list.add(sv);
        }
        return list;
    }

