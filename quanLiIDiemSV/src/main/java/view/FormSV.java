private void checkName(String ten) throws Exception{
    if(ten.length() == 0){
        throw new ArithmeticException("Khong duoc de trong!");
    }
    else if(ten.matches("[^a-zA-Z]+")){
        throw new Exception("Ten khong duoc chua ki tu la!");
    }
}
private void checkDate(String date) throws Exception{
    if(date.length() == 0){
        throw new ArithmeticException("Khong duoc de trong!");
    }
    else if(!date.matches("^\\d{2}([-|/])\\d{2}\\1\\d{4}$")){
        throw new Exception("Ngay sinh khong dung cu phap!");
    }
}
private void checkClass(String cls) throws Exception{
    if(cls.length() == 0){
        throw new ArithmeticException("Khong duoc de trong!");
    }
    else if(!cls.toUpperCase().matches("^D\\d{2}CQ(CN|AT|DT|VT)\\d{2}-B$")){
        throw new Exception("Lop khong dung cu phap!");
    }
}
