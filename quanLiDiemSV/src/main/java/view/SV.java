    private List<SinhVien> getAll() {
        List<SinhVien> list = new ArrayList<>();
        for (int i = 0; i < model.getRowCount(); i++) {
            int ma = Integer.parseInt(model.getValueAt(i, 0).toString());
            String ten = model.getValueAt(i, 1).toString();
            String dc = model.getValueAt(i, 2).toString();
            String ns = model.getValueAt(i, 3).toString();
            String lop = model.getValueAt(i, 4).toString();
            SinhVien sv = new SinhVien(ma, ten, dc, ns, lop);
            list.add(sv);
        }
        return list;
    }

    public void checkName(String name) throws Exception{
        if(name.length() == 0){
            throw new ArithmeticException("Khong duoc de trong!");
        }
        if (name.matches("[^a-zA-Z]+")) {
            throw new Exception("Ten khong duoc chua ky tu la!");
        }
    }

    public void checkDate(String date) throws Exception{
        if(date.length() == 0){
            throw new ArithmeticException("Khong duoc de trong!");
        }
        else if (!date.matches("^\\d{1,2}([./-])\\d{1,2}\\1\\d{4}$")) {
            throw new Exception("Ngay khong hop le!");
        }
    }

    public void checkClass(String cl) throws Exception{
        if(cl.length() == 0){
            throw new ArithmeticException("Khong duoc de trong!");
        }
        else if (!cl.toUpperCase().matches("^D\\d{2}CQ(CN|AT|VT|DT)\\d{2}-B$")) {
            throw new Exception("Lop khong dung cu phap!");
        }
    }
