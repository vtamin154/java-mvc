    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (add || fix) {
            SinhVien sv = new SinhVien();
            boolean isOk = true;
            int ma = Integer.parseInt(jTextField1.getText());
            String ten = jTextField2.getText();
            try {
                checkName(ten);
            } catch (Exception ex) {
                isOk = false;
                JOptionPane.showMessageDialog(this, "Ten khong hop le!");
                ex.printStackTrace();
            }
            String dc = jTextField3.getText();
            try {
                if(dc.length() == 0){
                    throw new ArithmeticException("Khong duoc de trong!");
                }
            } catch (Exception e) {
                isOk = false;
                JOptionPane.showMessageDialog(this, "Dia chi khong hop le!");
                e.printStackTrace();
            }
            String ns = jTextField4.getText();
            try {
                checkDate(ns);
            } catch (Exception e) {
                isOk = false;
                JOptionPane.showMessageDialog(this, "Ngay sinh khong hop le!");
                e.printStackTrace();
            }
            String lop = jTextField5.getText();
            try {
                checkClass(lop);
            } catch (Exception e) {
                isOk = false;
                JOptionPane.showMessageDialog(this, "Lop khong hop le!");
                e.printStackTrace();
            }
            if (isOk) {
                if (add) {
                    sv = new SinhVien(ma, ten, dc, ns, lop);
                    model.addRow(sv.toObject());
                    add = false;
                    jButton2.setEnabled(!add);
                    jButton1.setEnabled(add);
                } else {
                    int r = jTable1.getSelectedRow();
                    model.setValueAt(ten, r, 1);
                    model.setValueAt(dc, r, 2);
                    model.setValueAt(ns, r, 3);
                    model.setValueAt(lop, r, 4);
                    fix = false;
                    jButton1.setEnabled(fix);
                    jButton5.setEnabled(!fix);
                }
            } 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        if (r < 0 || r > jTable1.getRowCount() - 1) {
            JOptionPane.showMessageDialog(this, "Hay chon dong!");
        } else {
            fix = true;
            jButton5.setEnabled(!fix);
            jButton1.setEnabled(fix);
            jTextField2.setText(model.getValueAt(r, 1).toString());
            jTextField3.setText(model.getValueAt(r, 2).toString());
            jTextField4.setText(model.getValueAt(r, 3).toString());
            jTextField5.setText(model.getValueAt(r, 4).toString());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

