private List<ChamCong> getAll(){
    List<ChamCong> list = new ArrayList<>();
    for(int i = 0; i < model.getRowCount(); i++){
        int mcn = Integer.parseInt(model.getValueAt(i, 0).toString());
        int msp = Integer.parseInt(model.getValueAt(i, 2).toString());
        CongNhan cn = getCnByMa(mcn);
        SanPham sp = getSpByMa(msp);
        int sl = Integer.parseInt(model.getValueAt(i, 3).toString());
        ChamCong cc = new ChamCong(cn, sp,sl);
        list.add(cc);
    }
    return list;
}
private CongNhan getCnByMa(int mcn){
    List<CongNhan> list = IOFile.doc("src/main/java/io/CN.txt");
    for(CongNhan i : list){
        if(i.getMa() == mcn) return i;
    }
    return null;
}
private SanPham getSpByMa(int msp){
    List<SanPham> list = IOFile.doc("src/main/java/io/SP.txt");
    for(SanPham i : list){
        if(i.getMa() == msp) return i;
    }
    return null;
}
