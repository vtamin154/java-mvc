private boolean tontai(int mcn, int msp){
    List<ChamCong>list = getAll();
    for(int i = 0; i < list.size(); i++){
        if(list.get(i).getCn().getMa() == mcn && list.get(i).getSp().getMa() == msp){
            int sl = Integer.parseInt(jTextField1.getText())+
                    list.get(i).getSoLuong();
            model.setValueAt(sl, i, 3);
            return true;
        }
    }
    return false;
}
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int mcn = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int msp = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        if(!tontai(mcn, msp)){
            CongNhan cn = getCnByMa(mcn);
            SanPham sp = getSpByMa(msp);
            int sl = Integer.parseInt(jTextField1.getText());
            ChamCong cc = new ChamCong(cn,sp,sl);
            model.addRow(cc.toObject());
        }
    }//GEN-LAST:event_jButton1ActionPerformed


