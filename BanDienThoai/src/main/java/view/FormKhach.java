    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(model.getRowCount()> 0){
            KhachHang.setsMa(Integer.parseInt(model.getValueAt(model.getRowCount()-1, 0).toString()));
        }
        String ten = jTextField1.getText();
        boolean isOk  = true;
        try {
            if(ten.length() == 0|| ten.matches("[^a-zA-Z]+"))
                throw new Exception();
        } catch (Exception e) {
            isOk = false;
            JOptionPane.showMessageDialog(this, "Ten khong hop le!");
            e.printStackTrace();
        }
        String diaChi = jTextField2.getText();
        try {
            if(diaChi.length() == 0)
                throw new Exception();
        } catch (Exception e) {
            isOk = false;
            JOptionPane.showMessageDialog(this, "Dia chi khong hop le!");
            e.printStackTrace();
        }
        String loai = jComboBox1.getSelectedItem().toString();
        if(isOk){
            KhachHang khachHang = new KhachHang(ten, diaChi, loai);
            model.addRow(khachHang.toObject());
            list.add(khachHang);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


